$(document).ready(async () => {
  const connectWalletSection = $(".connect-wallet__section");
  const connectWalletButton = $(".connect-wallet__section .btn");

  const mintNftSection = $(".mint-nft__section");
  const mintNftForm = $(".mint-nft__form");
  const mintNftAmountTextField = $(".mint-nft__form input[name='amount']");
  const mintNftBtn = $(".mint-nft__form button");

  const processingSection = $(".processing__section");
  const processingMsg = $(".processing__msg");

  const errorSection = $(".error__section");
  const errorMsg = $(".error__msg");

  const successSection = $(".success__section");
  const successNftImg = $(".success__nft-img");

  connectWalletSection.hide();
  mintNftSection.hide();
  processingSection.hide();
  errorSection.hide();
  successSection.hide();

  const showConnectWallet = () => {
    mintNftSection.hide();
    processingSection.hide();
    errorSection.hide();
    successSection.hide();
    connectWalletSection.show();
  };

  const showMintNft = () => {
    connectWalletSection.hide();
    processingSection.hide();
    errorSection.hide();
    successSection.hide();
    mintNftSection.show();
  };

  const showProcessing = (msg) => {
    connectWalletSection.hide();
    mintNftSection.hide();
    errorSection.hide();
    successSection.hide();
    processingMsg.text(msg);
    processingSection.show();
  };

  const showSuccess = () => {
    connectWalletSection.hide();
    mintNftSection.hide();
    processingSection.hide();
    errorSection.hide();
    successSection.show();
  };

  const showError = (msg) => {
    connectWalletSection.hide();
    mintNftSection.hide();
    processingSection.hide();
    successSection.hide();
    errorMsg.text(msg);
    errorSection.show();
  };

  let provider;
  let signer;

  window.ethereum.addListener("connect", async (response) => {
    const chainId = parseInt(response.chainId);
    if (chainId !== 4) {
      showError("Please connect to the Rinkeby testnet");
      return;
    }
    provider = new ethers.providers.Web3Provider(window.ethereum);
    signer = provider.getSigner();
    account = (await provider.listAccounts())[0];
    if (account === undefined) {
      return;
    }
    showMintNft();
  });

  window.ethereum.on("accountsChanged", () => {
    window.location.reload();
  });

  window.ethereum.on("chainChanged", () => {
    window.location.reload();
  });

  window.ethereum.on("disconnect", () => {
    window.location.reload();
  });

  connectWalletButton.click(async () => {
    window.ethereum.request({ method: "eth_requestAccounts" });
  });

  mintNftBtn.click(async (event) => {
    event.preventDefault();
    const amountString = mintNftAmountTextField.val();
    const amount = Number.isNaN(amountString)
      ? undefined
      : Number(amountString);
    if (amount === undefined) {
      alert("Please enter a valid amount");
      return;
    }
    processingSection.show("Gethering details...");
    try {
      const contract = new ethers.Contract(
        contractAddress,
        contractAbi,
        signer
      );
      const mintPrice = await contract.mintPrice();
      const signerAddress = await signer.getAddress();
      const signerNonce = await signer.getTransactionCount();
      showProcessing("Please sign the transaction");
      const tx = await contract.safeMint(signerAddress, amount, {
        value: amount * mintPrice,
        nonce: signerNonce,
      });
      showProcessing("Minting NFT...");
      const txResult = await tx.wait();
      showSuccess();
      const tokenUri2 = await contract.tokenURI(
        Number(txResult.events[0].args[2])
      );
      const metadata = await (await fetch(tokenUri2)).json();
      successNftImg.attr("src", metadata.image);
    } catch (error) {
      showError("An error occurred: " + error.message);
    }
  });

  try {
    if (window.ethereum === undefined) {
      showError("Please install MetaMask");
      return;
    }
    provider = new ethers.providers.Web3Provider(ethereum);
    signer = provider.getSigner();
    showConnectWallet();
  } catch (error) {
    showError("An error occurred: " + error.message);
  }
});

